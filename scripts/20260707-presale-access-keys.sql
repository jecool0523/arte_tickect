CREATE EXTENSION IF NOT EXISTS pgcrypto WITH SCHEMA extensions;

CREATE TABLE IF NOT EXISTS public.presale_access_keys (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  musical_id TEXT NOT NULL,
  key_hash TEXT NOT NULL,
  label TEXT,
  starts_at TIMESTAMPTZ,
  ends_at TIMESTAMPTZ,
  max_uses INTEGER,
  max_seats_per_booking INTEGER,
  used_count INTEGER NOT NULL DEFAULT 0,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT presale_access_keys_max_uses_positive CHECK (max_uses IS NULL OR max_uses > 0),
  CONSTRAINT presale_access_keys_max_seats_positive CHECK (max_seats_per_booking IS NULL OR max_seats_per_booking > 0),
  CONSTRAINT presale_access_keys_used_count_nonnegative CHECK (used_count >= 0),
  CONSTRAINT presale_access_keys_time_range CHECK (starts_at IS NULL OR ends_at IS NULL OR starts_at <= ends_at)
);

CREATE INDEX IF NOT EXISTS idx_presale_access_keys_musical_id
  ON public.presale_access_keys (musical_id);

CREATE INDEX IF NOT EXISTS idx_presale_access_keys_active_window
  ON public.presale_access_keys (musical_id, is_active, starts_at, ends_at);

ALTER TABLE public.presale_access_keys ENABLE ROW LEVEL SECURITY;

