CREATE OR REPLACE FUNCTION public.touch_presale_access_keys_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS touch_presale_access_keys_updated_at ON public.presale_access_keys;
CREATE TRIGGER touch_presale_access_keys_updated_at
  BEFORE UPDATE ON public.presale_access_keys
  FOR EACH ROW
  EXECUTE FUNCTION public.touch_presale_access_keys_updated_at();

CREATE OR REPLACE FUNCTION public.create_presale_access_key(
  p_musical_id TEXT,
  p_key TEXT,
  p_label TEXT DEFAULT NULL,
  p_starts_at TIMESTAMPTZ DEFAULT NULL,
  p_ends_at TIMESTAMPTZ DEFAULT NULL,
  p_max_uses INTEGER DEFAULT NULL
)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
DECLARE
  v_key_id BIGINT;
BEGIN
  IF p_musical_id IS NULL OR BTRIM(p_musical_id) = '' THEN
    RAISE EXCEPTION 'musical_id is required';
  END IF;

  IF p_key IS NULL OR LENGTH(BTRIM(p_key)) < 6 THEN
    RAISE EXCEPTION 'presale key must be at least 6 characters';
  END IF;

  INSERT INTO public.presale_access_keys (
    musical_id,
    key_hash,
    label,
    starts_at,
    ends_at,
    max_uses
  )
  VALUES (
    BTRIM(p_musical_id),
    extensions.crypt(BTRIM(p_key), extensions.gen_salt('bf')),
    NULLIF(BTRIM(p_label), ''),
    p_starts_at,
    p_ends_at,
    p_max_uses
  )
  RETURNING id INTO v_key_id;

  RETURN v_key_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.consume_presale_access_key(
  p_musical_id TEXT,
  p_key TEXT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
DECLARE
  v_key_id BIGINT;
BEGIN
  IF p_musical_id IS NULL OR p_key IS NULL OR BTRIM(p_key) = '' THEN
    RETURN FALSE;
  END IF;

  SELECT id
    INTO v_key_id
  FROM public.presale_access_keys
  WHERE musical_id = BTRIM(p_musical_id)
    AND is_active = TRUE
    AND key_hash = extensions.crypt(BTRIM(p_key), key_hash)
    AND (starts_at IS NULL OR NOW() >= starts_at)
    AND (ends_at IS NULL OR NOW() <= ends_at)
    AND (max_uses IS NULL OR used_count < max_uses)
  ORDER BY id
  FOR UPDATE SKIP LOCKED
  LIMIT 1;

  IF v_key_id IS NULL THEN
    RETURN FALSE;
  END IF;

  UPDATE public.presale_access_keys
  SET used_count = used_count + 1
  WHERE id = v_key_id;

  RETURN TRUE;
END;
$$;

CREATE OR REPLACE FUNCTION public.release_presale_access_key(
  p_musical_id TEXT,
  p_key TEXT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
DECLARE
  v_key_id BIGINT;
BEGIN
  IF p_musical_id IS NULL OR p_key IS NULL OR BTRIM(p_key) = '' THEN
    RETURN FALSE;
  END IF;

  SELECT id
    INTO v_key_id
  FROM public.presale_access_keys
  WHERE musical_id = BTRIM(p_musical_id)
    AND key_hash = extensions.crypt(BTRIM(p_key), key_hash)
    AND used_count > 0
  ORDER BY id
  FOR UPDATE SKIP LOCKED
  LIMIT 1;

  IF v_key_id IS NULL THEN
    RETURN FALSE;
  END IF;

  UPDATE public.presale_access_keys
  SET used_count = GREATEST(used_count - 1, 0)
  WHERE id = v_key_id;

  RETURN TRUE;
END;
$$;

CREATE OR REPLACE FUNCTION public.validate_presale_access_key(
  p_musical_id TEXT,
  p_key TEXT
)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public, extensions
AS $$
  SELECT p_musical_id IS NOT NULL
    AND p_key IS NOT NULL
    AND BTRIM(p_key) <> ''
    AND EXISTS (
      SELECT 1
      FROM public.presale_access_keys
      WHERE musical_id = BTRIM(p_musical_id)
        AND is_active = TRUE
        AND key_hash = extensions.crypt(BTRIM(p_key), key_hash)
        AND (starts_at IS NULL OR NOW() >= starts_at)
        AND (ends_at IS NULL OR NOW() <= ends_at)
        AND (max_uses IS NULL OR used_count < max_uses)
    );
$$;

CREATE OR REPLACE FUNCTION public.get_presale_access_key_seat_limit(
  p_musical_id TEXT,
  p_key TEXT
)
RETURNS INTEGER
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public, extensions
AS $$
  SELECT max_seats_per_booking
  FROM public.presale_access_keys
  WHERE p_musical_id IS NOT NULL
    AND p_key IS NOT NULL
    AND BTRIM(p_key) <> ''
    AND musical_id = BTRIM(p_musical_id)
    AND is_active = TRUE
    AND key_hash = extensions.crypt(BTRIM(p_key), key_hash)
    AND (starts_at IS NULL OR NOW() >= starts_at)
    AND (ends_at IS NULL OR NOW() <= ends_at)
    AND (max_uses IS NULL OR used_count < max_uses)
  ORDER BY id
  LIMIT 1;
$$;

